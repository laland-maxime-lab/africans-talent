function setLanguage(lang) {
    // Update Text
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });

    // Update Placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[lang] && translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });

    // Update Currency and Prices based on data-price-cfa
    document.querySelectorAll('[data-price-cfa]').forEach(el => {
        const cfa = parseInt(el.getAttribute('data-price-cfa'));
        if (lang === 'fr') {
            el.textContent = new Intl.NumberFormat('fr-FR').format(cfa) + " FCFA";
        } else if (lang === 'en') {
            // Conversion 1 USD = 600 FCFA
            const usd = Math.round(cfa / 600);
            el.textContent = new Intl.NumberFormat('en-US', { style: 'currency', currency: 'USD' }).format(usd);
        } else if (lang === 'ru') {
            // Conversion 1 RUB = 6.5 FCFA
            const rub = Math.round(cfa / 6.5);
            el.textContent = new Intl.NumberFormat('ru-RU', { style: 'currency', currency: 'RUB' }).format(rub);
        }
    });

    // Update Flag and UI
    const flagEl = document.getElementById('current-lang-flag');
    const textEl = document.getElementById('current-lang-text');

    if (flagEl && textEl) {
        const flags = { fr: '🇫🇷', en: '🇺🇸', ru: '🇷🇺' };
        const texts = { fr: 'FR', en: 'EN', ru: 'RU' };
        flagEl.textContent = flags[lang];
        textEl.textContent = texts[lang];
    }

    localStorage.setItem('preferredLang', lang);
}

function toggleTheme() {
    const html = document.documentElement;
    if (html.classList.contains('dark')) {
        html.classList.remove('dark');
        localStorage.setItem('theme', 'light');
    } else {
        html.classList.add('dark');
        localStorage.setItem('theme', 'dark');
    }
}

function initializeTheme() {
    const theme = localStorage.getItem('theme');
    if (theme === 'dark' || (!theme && window.matchMedia('(prefers-color-scheme: dark)').matches)) {
        document.documentElement.classList.add('dark');
    } else {
        document.documentElement.classList.remove('dark');
    }
}

